import logging
import os
import time
from contextlib import asynccontextmanager
from typing import Literal, Optional

from fastapi import FastAPI, HTTPException
from fastapi.middleware.cors import CORSMiddleware
from pydantic import BaseModel, Field

from scoring_engine import ScoringEngine

# ── Logging ───────────────────────────────────────────────────────────────────
log_level = os.getenv("GUARDIAN_LOG_LEVEL", "INFO").upper()
logging.basicConfig(level=log_level, format="%(asctime)s | %(levelname)s | %(message)s")
log = logging.getLogger(__name__)

# ── Model path (override via env var for Docker / deployment) ─────────────────
MODEL_PATH = os.getenv("GUARDIAN_MODEL_PATH", "model.joblib")


# ─────────────────────────────────────────────────────────────────────────────
# PYDANTIC REQUEST SCHEMA
# ─────────────────────────────────────────────────────────────────────────────
# Field names match EXACTLY the columns in fraud_synthetic_dataset.csv,
# minus transaction_id, user_id (identifiers), and is_fraud_label (target).
#
# This class is the CONTRACT between the backend team (Member 1) and the ML
# service.  Any change here must be mirrored in scoring_engine.py and train.py.
# ─────────────────────────────────────────────────────────────────────────────
class TransactionRequest(BaseModel):
    """
    Input payload for the /predict endpoint.

    All field names come directly from fraud_synthetic_dataset.csv.
    Default values let the frontend omit optional context without errors.

    GDPR note: no personal data (name, IBAN, e-mail) is accepted here.
    Only anonymised behavioural signals are processed by the model.
    """

    transaction_amount: float = Field(
        ...,                        # required — no default
        ge=0,
        description="Transaction value in BGN.  Must be ≥ 0.",
        examples=[250.00],
    )
    hour_of_day: int = Field(
        ...,                        # required
        ge=0,
        le=23,
        description="Hour of day (0–23) when the transaction was initiated.",
        examples=[14],
    )
    is_new_device: int = Field(
        default=0,
        ge=0,
        le=1,
        description="1 if this device fingerprint was never seen for this user, else 0.",
        examples=[0],
    )
    is_new_ip: int = Field(
        default=0,
        ge=0,
        le=1,
        description="1 if this IP address was never seen for this user, else 0.",
        examples=[0],
    )
    is_foreign_country: int = Field(
        default=0,
        ge=0,
        le=1,
        description="1 if the transaction originates from outside the user's home country.",
        examples=[0],
    )
    distance_from_usual_km: float = Field(
        default=0.0,
        ge=0,
        description="Kilometres between this transaction's location and the user's typical location.",
        examples=[12.5],
    )
    is_phishing_detected: int = Field(
        default=0,
        ge=0,
        le=1,
        description=(
            "1 if a URL in the current session matched the phishing/smishing blocklist.  "
            "Triggers an immediate BLOCK via the Rule-Based Engine."
        ),
        examples=[0],
    )
    detected_url: Optional[str] = Field(
        default=None,
        description=(
            "Raw URL extracted from the current session (e.g. from a clicked link).  "
            "Scanned by the heuristic phishing engine for banking-lookalike domains "
            "and known URL shorteners.  No PII — URL only."
        ),
        examples=["https://fibank-verify.ru/secure-login"],
    )
    sms_payload: Optional[str] = Field(
        default=None,
        description=(
            "Raw SMS text forwarded by the mobile app for smishing analysis.  "
            "Scanned for high-urgency Albanian and banking-scam keywords.  "
            "Content is never stored — only a binary threat signal is produced."
        ),
        examples=["Ju lutem verifikoni llogarinë tuaj në Fibank urgjentisht."],
    )

    model_config = {
        # Reject unknown fields — prevents the backend from silently sending
        # extra data that the model ignores (a common integration bug).
        "extra": "forbid",

        # Example shown in /docs Swagger UI — useful for the demo.
        "json_schema_extra": {
            "examples": [
                {
                    "summary": "Normal transaction",
                    "value": {
                        "transaction_amount": 85.50,
                        "hour_of_day": 14,
                        "is_new_device": 0,
                        "is_new_ip": 0,
                        "is_foreign_country": 0,
                        "distance_from_usual_km": 5.2,
                        "is_phishing_detected": 0,
                    },
                },
                {
                    "summary": "Suspicious transaction (phishing + foreign + new device)",
                    "value": {
                        "transaction_amount": 1800.00,
                        "hour_of_day": 2,
                        "is_new_device": 1,
                        "is_new_ip": 1,
                        "is_foreign_country": 1,
                        "distance_from_usual_km": 9500.0,
                        "is_phishing_detected": 1,
                    },
                },
            ]
        },
    }


# ─────────────────────────────────────────────────────────────────────────────
# PYDANTIC RESPONSE SCHEMA
# ─────────────────────────────────────────────────────────────────────────────
class PredictionResponse(BaseModel):
    """
    Response from the /predict endpoint.

    Fields
    ------
    action       : The decision — what the frontend/backend should do next.
    reason       : Plain-English explanation (GDPR Article 22 compliance).
    score        : Raw Isolation Forest anomaly score.
                   Negative = more anomalous, positive = more normal.
                   0.0 when the decision was made by a hard rule (no ML needed).
    latency_ms   : Server-side inference time in milliseconds.
    """
    action:     Literal["ALLOW", "MFA_CHALLENGE", "BLOCK"]
    reason:     str
    score:      float
    latency_ms: float


# ─────────────────────────────────────────────────────────────────────────────
# APPLICATION LIFESPAN — load model once at startup, not per-request
# ─────────────────────────────────────────────────────────────────────────────
_engine: ScoringEngine | None = None


@asynccontextmanager
async def lifespan(app: FastAPI):
    """
    Load the ScoringEngine once when the server boots.
    Using lifespan (not deprecated @app.on_event) is the FastAPI 0.95+ pattern.
    """
    global _engine
    log.info("GuardianAI API starting up …")
    try:
        _engine = ScoringEngine(MODEL_PATH)
        log.info("✓ ScoringEngine loaded and ready.")
    except FileNotFoundError:
        log.critical(
            f"Model file not found: '{MODEL_PATH}'.  "
            "Run `python train.py` first, then restart the API."
        )
        raise   # prevent the server from starting in a broken state
    yield
    log.info("GuardianAI API shutting down.")


