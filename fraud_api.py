# ─────────────────────────────────────────────────────────────────────────────
# ENDPOINTS
# ─────────────────────────────────────────────────────────────────────────────

@app.get("/health", tags=["ops"], summary="Liveness probe")
def health():
    """
    Returns 200 OK when the scoring engine is loaded.
    Returns 503 if the model has not been initialised yet.
    Use this as the health check URL in Docker / Kubernetes.
    """
    if _engine is None:
        raise HTTPException(status_code=503, detail="Scoring engine not loaded.")
    return {"status": "ok", "model_loaded": True}


@app.post(
    "/predict",
    response_model=PredictionResponse,
    tags=["fraud-detection"],
    summary="Score a transaction event",
    response_description=(
        "Returns ALLOW, MFA_CHALLENGE, or BLOCK with a GDPR-compliant reason string."
    ),
)
def predict(request: TransactionRequest) -> PredictionResponse:
    """
    **Main endpoint** — receives a transaction payload and returns a fraud decision.

    ### Decision logic (in priority order):

    1. **Rule-Based Engine** — deterministic hard rules evaluated first.
       - Rule 1: `is_phishing_detected == 1`  →  **BLOCK** (Smishing protection)

    2. **Isolation Forest** — statistical anomaly scoring.
       - `predict() == -1` (anomaly)  →  **MFA_CHALLENGE**
       - `predict() == +1` (normal)   →  **ALLOW**

    ### GDPR compliance:
    - No personal identifiers are accepted or stored.
    - The `reason` field provides a specific, human-readable justification.
    - All requests are logged (no PII) for the audit trail.

    ### Integration (Member 1 / Backend):
    ```
    POST /predict
    Content-Type: application/json

    {
      "transaction_amount": 250.00,
      "hour_of_day": 14,
      "is_new_device": 0,
      "is_new_ip": 0,
      "is_foreign_country": 0,
      "distance_from_usual_km": 5.2,
      "is_phishing_detected": 0
    }
    ```
    """
    if _engine is None:
        raise HTTPException(status_code=503, detail="Scoring engine not available.")

    # ── Convert Pydantic model → plain dict for the scoring engine ────────────
    tx_dict = request.model_dump()

    t0 = time.perf_counter()

    try:
        result = _engine.evaluate_transaction(tx_dict)
    except Exception as exc:
        # Log full stack trace server-side; return a clean 500 to the caller.
        log.exception("Unexpected error in ScoringEngine.evaluate_transaction()")
        raise HTTPException(status_code=500, detail=f"Scoring failed: {exc}") from exc

    latency_ms = (time.perf_counter() - t0) * 1_000

    # ── Audit log — no PII, just the decision and key signals ─────────────────
    log.info(
        "AUDIT | action=%-14s | score=%+.4f | latency=%.1fms | "
        "amount=%.2f | hour=%d | new_device=%d | new_ip=%d | foreign=%d | phishing=%d",
        result["action"],
        result.get("score", 0.0),
        latency_ms,
        tx_dict["transaction_amount"],
        tx_dict["hour_of_day"],
        tx_dict["is_new_device"],
        tx_dict["is_new_ip"],
        tx_dict["is_foreign_country"],
        tx_dict["is_phishing_detected"],
    )

    return PredictionResponse(
        action     = result["action"],
        reason     = result["reason"],
        score      = round(result.get("score", 0.0), 6),
        latency_ms = round(latency_ms, 3),
    )


